def format_numbers(numbers):
    """
    Formats a list of numbers with spaces, and adds a newline after every 12 numbers.

    Args:
        numbers (list): List of integers to format.

    Returns:
        str: A formatted string with numbers separated by spaces and grouped in rows of 12.
    """
    formatted_lines = []
    for i in range(0, len(numbers), 12):
        line = " ".join(map(str, numbers[i:i+12]))
        formatted_lines.append(line)
    return "\n".join(formatted_lines)

